#The purpose of the "027.py" script is to convert all signed bytes into 16-bit signed integers.
#Then, by dividing the obtained values by 32768, we can convert them into real and imaginary values
#ranging from -1 to 1. The script also utilizes the W factor for an FFT length of 1024 to determine the value of k for each twiddle.

#For the "etwiddle" case, there are 256 * 3 twiddles, resulting in a total of 768 twiddles.
#Similarly, for the "ptwiddle" case, there are 64 * 3 twiddles, resulting in a total of 192
#twiddles. Overall, there are 960 twiddles in total, each associated with a specific value of k.
# 256 * 3 = 768 for e twiddle
# 64 * 3 = 192 for p twiddle
# 320*3 = 960 for all the twiddle
#

import cmath

def find_k_from_W(W, N):
    # Calculate the phase angle of the W factor
    phase_angle = cmath.phase(W)
    
    # Calculate the value of k
    k = round(-phase_angle * N / (2 * cmath.pi)) % N
    
    return k



